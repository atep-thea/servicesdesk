    public function execute()
    {
        $campaign = Campaign::all();
        $toDay = date('Y-m-d');

        foreach($campaign as $data){
            $batas = $data->end_date;
            $id = $data->id;
            if($batas == $toDay){
                DB::update("UPDATE campaigns SET  status = 'Draft' WHERE id = '".$id."'");
            }
        }
        
    }


