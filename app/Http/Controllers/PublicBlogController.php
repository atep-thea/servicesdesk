    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $blog = Blog::find($id);

        if (!$blog) return view('errors.404');

        \Carbon\Carbon::setLocale('id');
        $post_date = new Carbon($blog->updated_at);
        $blog['post_date'] = $post_date->diffForHumans();

        return view('public.showBlog', compact('blog'));
    }

